def compute_precision_recall(pred_edges, gt_edges, tolerance=2):
    """
    Precision ve Recall hesaplar.
    tolerance: kaç piksel tolerans (BSDS standardı: 2px)
    """
    from scipy.ndimage import binary_dilation

    # Ground truth'u biraz genişlet (tolerans için)
    gt_dilated = binary_dilation(gt_edges, iterations=tolerance)
    pred_dilated = binary_dilation(pred_edges, iterations=tolerance)

    # Precision: tahmin edilen kenarların ne kadarı gerçek
    if pred_edges.sum() == 0:
        precision = 0.0
    else:
        precision = (pred_edges & gt_dilated).sum() / pred_edges.sum()

    # Recall: gerçek kenarların ne kadarı bulundu
    if gt_edges.sum() == 0:
        recall = 0.0
    else:
        recall = (gt_edges & pred_dilated).sum() / gt_edges.sum()

    # F-measure
    if precision + recall == 0:
        f_measure = 0.0
    else:
        f_measure = 2 * precision * recall / (precision + recall)

    return precision, recall, f_measure

def load_ground_truth(img_filename, gt_dir):
    """
    BSDS500 ground truth'u yükler (.mat dosyası)
    """
    import scipy.io as sio

    img_id = img_filename.replace('.jpg', '')
    gt_path = os.path.join(gt_dir, img_id + '.mat')

    if not os.path.exists(gt_path):
        return None

    mat = sio.loadmat(gt_path)
    # BSDS500'de birden fazla annotator var, ortalamasını al
    gt_data = mat['groundTruth']
    boundaries = []
    for i in range(gt_data.shape[1]):
        boundary = gt_data[0, i]['Boundaries'][0, 0]
        boundaries.append(boundary)

    # Annotator'ların union'ı
    gt_combined = np.zeros_like(boundaries[0], dtype=bool)
    for b in boundaries:
        gt_combined = gt_combined | (b > 0)

    return gt_combined

