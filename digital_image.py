def ncc_ablation_A(image, alpha=0.8, beta=0.4, gamma=2.0):
    """NCC-A: Sadece adaptive smoothing, sabit Otsu threshold"""
    start = time.time()
    sigma_est = estimate_noise(image)
    sigma_g = np.clip(0.5 * sigma_est * 255, 0.5, 3.0)
    smoothed = gaussian(image, sigma=sigma_g)
    grad = sobel(smoothed)
    # Threshold: Otsu (sabit, noise-aware değil)
    t_high = threshold_otsu(grad)
    t_low = 0.5 * t_high
    edges = canny(image, sigma=sigma_g,
                  low_threshold=t_low, high_threshold=t_high)
    return edges, time.time() - start

def ncc_ablation_B(image, alpha=0.8, beta=0.4, gamma=2.0):
    """NCC-B: Sabit smoothing, sadece adaptive threshold"""
    start = time.time()
    sigma_est = estimate_noise(image)
    sigma_g = 1.0  # sabit
    smoothed = gaussian(image, sigma=sigma_g)
    grad = sobel(smoothed)
    mu_grad = np.mean(grad)
    t_high = alpha * (mu_grad + gamma * sigma_est)
    t_low = beta * t_high
    edges = canny(image, sigma=sigma_g,
                  low_threshold=t_low, high_threshold=t_high)
    return edges, time.time() - start

