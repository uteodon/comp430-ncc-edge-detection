def add_gaussian_noise(image, sigma):
    """
    Görüntüye Gaussian gürültü ekler.
    sigma: gürültü standart sapması (0-255 ölçeğinde)
    """
    # skimage random_noise 0-1 aralığında çalışır
    noise_var = (sigma / 255.0) ** 2
    noisy = random_noise(image, mode='gaussian', var=noise_var)
    return noisy

