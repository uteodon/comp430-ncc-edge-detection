# ── Baseline 1: Sobel ──────────────────────────────────────────────
def sobel_detector(image):
    start = time.time()
    edge_map = sobel(image)
    # Sobel çıktısını binary'ye çevir (Otsu ile threshold)
    thresh = threshold_otsu(edge_map)
    binary = edge_map > thresh
    runtime = time.time() - start
    return binary, runtime

# ── Baseline 2: Standard Canny (sabit parametreler) ────────────────
def standard_canny(image, sigma=1.0, low_threshold=0.05, high_threshold=0.15):
    start = time.time()
    edge_map = canny(image, sigma=sigma,
                     low_threshold=low_threshold,
                     high_threshold=high_threshold)
    runtime = time.time() - start
    return edge_map, runtime

# ── Baseline 3: Otsu-Canny (Medina-Carnicer et al., 2011) ──────────
def otsu_canny(image, sigma=1.0):
    start = time.time()
    # Önce smooth et
    smoothed = gaussian(image, sigma=sigma)
    # Gradient magnitude hesapla
    grad = sobel(smoothed)
    # Gradient üzerine Otsu uygula
    high_threshold = threshold_otsu(grad)
    low_threshold = 0.5 * high_threshold
    edge_map = canny(image, sigma=sigma,
                     low_threshold=low_threshold,
                     high_threshold=high_threshold)
    runtime = time.time() - start
    return edge_map, runtime

