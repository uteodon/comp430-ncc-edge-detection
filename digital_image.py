def estimate_noise(image):
    """
    MAD tabanlı noise tahmini.
    Laplacian residual üzerinden sigma_est hesaplar.
    """
    smoothed = gaussian(image, sigma=1.0)
    residual = image - smoothed
    median = np.median(residual)
    sigma_est = np.median(np.abs(residual - median)) / 0.6745
    sigma_est = max(sigma_est, 1.0 / 255.0)  # çok küçük değerleri önle
    return sigma_est

def noise_calibrated_canny(image, alpha=0.8, beta=0.4, gamma=2.0, k=0.5):
    """
    Proposed method: Noise-Calibrated Canny (NCC)
    """
    start = time.time()

    # Stage 1: Noise estimation
    sigma_est = estimate_noise(image)

    # Stage 2: Adaptive Gaussian smoothing
    sigma_g = k * sigma_est * 255  # 0-1 ölçeğinden 0-255'e
    sigma_g = np.clip(sigma_g, 0.5, 3.0)
    smoothed = gaussian(image, sigma=sigma_g)

    # Stage 3: Gradient computation
    grad = sobel(smoothed)
    mu_grad = np.mean(grad)

    # Stage 4: Noise-proportional threshold selection
    # T_high = alpha * (mu_grad + gamma * sigma_est)
    # T_low  = beta * T_high
    t_high = alpha * (mu_grad + gamma * sigma_est)
    t_low  = beta * t_high

    edge_map = canny(image, sigma=sigma_g,
                     low_threshold=t_low,
                     high_threshold=t_high)

    runtime = time.time() - start
    return edge_map, runtime, sigma_est, sigma_g, t_high, t_low

