def plot_precision_recall(results):
    sigma_levels = [5, 10, 25]
    methods = {
        'sobel': 'Sobel',
        'standard_canny': 'Standard Canny',
        'otsu_canny': 'Otsu-Canny',
        'ncc': 'NCC (Proposed)'
    }
    colors = ['blue', 'green', 'orange', 'red']
    markers = ['o', 's', '^', 'D']

    fig, axes = plt.subplots(1, 3, figsize=(15, 5))

    for idx, sigma in enumerate(sigma_levels):
        ax = axes[idx]
        for (method_key, method_name), color, marker in zip(methods.items(), colors, markers):
            vals = results[method_key][sigma]
            avg_p = np.mean([v[0] for v in vals])
            avg_r = np.mean([v[1] for v in vals])
            lw = 2.5 if method_key == 'ncc' else 1.5
            ax.scatter(avg_r, avg_p, color=color, marker=marker, s=100, label=method_name, zorder=5)

        ax.set_xlabel('Recall', fontsize=11)
        ax.set_ylabel('Precision', fontsize=11)
        ax.set_title(f'Precision-Recall (σ={sigma})', fontsize=12)
        ax.legend(fontsize=8)
        ax.set_xlim(0, 1.05)
        ax.set_ylim(0, 0.65)
        ax.grid(True, alpha=0.3)

    plt.suptitle('Precision-Recall Comparison Across Noise Levels', fontsize=13)
    plt.tight_layout()
    plt.savefig('precision_recall.png', dpi=150, bbox_inches='tight')
