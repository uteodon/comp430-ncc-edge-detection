def run_benchmark(img_files, img_dir, gt_dir, sigma_levels=[5, 10, 25], n_images=50):
    """
    Tüm yöntemleri tüm noise seviyelerinde değerlendirir.
    """
    results = {
        'sobel': {s: [] for s in sigma_levels},
        'standard_canny': {s: [] for s in sigma_levels},
        'otsu_canny': {s: [] for s in sigma_levels},
        'ncc': {s: [] for s in sigma_levels},
    }
    runtimes = {
        'sobel': [], 'standard_canny': [], 'otsu_canny': [], 'ncc': []
    }

    # İlk n_images kadar görüntü kullan
    selected_files = img_files[:n_images]

    for i, fname in enumerate(selected_files):
        print(f"İşleniyor: {i+1}/{n_images} - {fname}", end='\r')

        # Ground truth yükle
        gt = load_ground_truth(fname, gt_dir)
        if gt is None:
            continue

        # Orijinal görüntüyü yükle
        img = io.imread(os.path.join(img_dir, fname))
        img_gray = color.rgb2gray(img)

        for sigma in sigma_levels:
            noisy = add_gaussian_noise(img_gray, sigma)

            # Sobel
            edges, rt = sobel_detector(noisy)
            p, r, f = compute_precision_recall(edges, gt)
            results['sobel'][sigma].append((p, r, f))
            if sigma == sigma_levels[0]:
                runtimes['sobel'].append(rt)

            # Standard Canny
            edges, rt = standard_canny(noisy)
            p, r, f = compute_precision_recall(edges, gt)
            results['standard_canny'][sigma].append((p, r, f))
            if sigma == sigma_levels[0]:
                runtimes['standard_canny'].append(rt)

            # Otsu-Canny
            edges, rt = otsu_canny(noisy)
            p, r, f = compute_precision_recall(edges, gt)
            results['otsu_canny'][sigma].append((p, r, f))
            if sigma == sigma_levels[0]:
                runtimes['otsu_canny'].append(rt)

            # NCC (Proposed)
            edges, rt, _, _, _, _ = noise_calibrated_canny(noisy)
            p, r, f = compute_precision_recall(edges, gt)
            results['ncc'][sigma].append((p, r, f))
            if sigma == sigma_levels[0]:
                runtimes['ncc'].append(rt)

    print("\nTamamlandı!")
    return results, runtimes

